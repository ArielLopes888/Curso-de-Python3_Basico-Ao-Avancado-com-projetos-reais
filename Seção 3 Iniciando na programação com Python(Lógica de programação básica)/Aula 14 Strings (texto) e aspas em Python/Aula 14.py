# string é um dado primitivo em python, string é tudo que estiver em aspas
print("Essa é uma 'string' (str).")
print('Essa é uma "string" (str).')  # mais recomendado
print("Essa é uma \"string\" (str).")  # caractere de escape, não recomendável por ser feio
print("Essa é uma \n 'string' (str).")  # \n quebra texto
print(r"Essa é uma \n 'string' (str).")  # r para não executar código depois dele
# sempre fechar tudo que abrir
